package Csla.Server.Hosts.MetroChannel;

/**
 * Response message for returning the results of a data portal call.
 * 
 * 
 * @author Eric
 * @version 1.0
 * @created 21-Dec-2009 7:10:49 PM
 */
public class MetroResponse {

	private Object _result;


	/**
	 * Create new instance of Object.
	 * 
	 * @param result    Result Object to be returned.
	 */
	public MetroResponse(Object result){
		_result = result;
	}

	/**
	 * Criteria Object describing business Object.
	 */
	public Object getResult(){
		return _result; 
	}
	public void setResult(Object value){
		_result = value; 
	}
}